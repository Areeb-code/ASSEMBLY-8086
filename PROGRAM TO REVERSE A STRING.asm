;a program  to ENTER A STRING IN REVERSE ORDER
.MODEL SMALL
.STACK 100H
.DATA
  MSG DB 'HELLO PAKISTAN!$'
  LENGTH EQU $-MSG
.CODE
MAIN PROC FAR
  MOV AX, @DATA
  MOV DS, AX
  
  LEA DX, MSG
  MOV AH, 09H
  INT 21H
  
  MOV SI, 0
  MOV CX, LENGTH
  DEC CX
  
AGAIN:
  PUSH [SI]
  INC SI
  LOOP AGAIN
  
  MOV SI, 0
  MOV CX, LENGTH
  DEC CX
  
POP_LOOP:
  POP DX
  MOV AH, 02H
  INT 21H
  LOOP POP_LOOP

  MOV AH, 4CH
  INT 21H
  
MAIN ENDP
END MAIN
         